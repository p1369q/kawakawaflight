import { execFileSync } from 'node:child_process';
import { mkdir, readFile, writeFile } from 'node:fs/promises';

const packageJson = JSON.parse(await readFile('package.json', 'utf8'));
const now = new Date();
const jstParts = new Intl.DateTimeFormat('en-CA', {
  timeZone: 'Asia/Tokyo', year: 'numeric', month: '2-digit', day: '2-digit',
  hour: '2-digit', minute: '2-digit', hourCycle: 'h23',
}).formatToParts(now).reduce((values, part) => ({ ...values, [part.type]: part.value }), {});
const date = `${jstParts.year}-${jstParts.month}-${jstParts.day}`;
const builtAt = `${date} ${jstParts.hour}:${jstParts.minute} JST`;
const buildSequence = process.env.GITHUB_RUN_NUMBER || process.env.BUILD_NUMBER || '01';
const buildNumber = `${date.replaceAll('-', '.')}-${String(buildSequence).padStart(2, '0')}`;

function gitCommit() {
  if (process.env.BUILD_INFO_SKIP_GIT === '1') return 'local-build';
  if (process.env.BUILD_INFO_COMMIT) return process.env.BUILD_INFO_COMMIT;
  try {
    return execFileSync('git', ['rev-parse', '--short', 'HEAD'], { encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'] }).trim() || 'local-build';
  } catch {
    return 'local-build';
  }
}

const buildInfo = { version: packageJson.version, buildNumber, commit: gitCommit(), builtAt };
await mkdir('src/generated', { recursive: true });
await writeFile('src/generated/buildInfo.ts', `// Generated by scripts/generate-build-info.mjs. Do not edit manually.\nexport const buildInfo = ${JSON.stringify(buildInfo, null, 2)} as const;\n`);
console.log(`Generated build info: v${buildInfo.version} · ${buildInfo.commit} (${buildInfo.buildNumber})`);
